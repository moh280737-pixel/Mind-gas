function startGame(){

document.getElementById("intro").style.display="none";
document.getElementById("app").style.display="block";

home();

}



function update(){

document.getElementById("score").innerHTML=score;
document.getElementById("coins").innerHTML=coins;
document.getElementById("level").innerHTML=level;

}



function show(html){

document.getElementById("game").innerHTML=html;

}



function win(){

score += 10;
coins += 5;


if(score >= level*100){

level++;
alert("🎉 تم فتح مستوى جديد");

}


update();

}



function lose(){

alert("❌ إجابة خاطئة");

}



function home(){

document.getElementById("title").innerHTML="القائمة";


show(`

<button onclick="quiz()">🧠 الأسئلة</button>

<button onclick="puzzle()">🧩 الألغاز</button>

<button onclick="shop()">🛒 المتجر</button>

<button onclick="settings()">⚙️ الإعدادات</button>

`);

}



function quiz(){

let r=Math.floor(Math.random()*questions.length);

let q=questions[r];


document.getElementById("title").innerHTML="🧠 سؤال";


let html="<p>"+q[0]+"</p>";


q[1].forEach((a,i)=>{

html += `

<button onclick="answer(${i},${q[2]})">

${a}

</button>

`;

});


html += `<button onclick="home()">🏠 القائمة</button>`;


show(html);

}



function answer(a,b){

if(a==b){

win();

setTimeout(()=>{

quiz();

},500);


}else{

lose();

}

}




